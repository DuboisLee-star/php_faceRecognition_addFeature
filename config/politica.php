<p align="justify"><b>SEÇÃO 1 – INFORMAÇÕES GERAIS</b><br><br>

O Clube de Tiro está empenhado em salvaguardar a sua privacidade ao utilizar nossos serviços e plataformas. Respeitamos sua privacidade e estamos comprometidos em tomar todas as medidas para preservá-la, desta forma criamos essa Política para dar visibilidade de todo o ciclo de vida do seu dado pessoal (coleta, uso, armazenamento, tratamento, proteção e exclusão) vinculado a nossos serviços.<br><br>

Deixamos a disposição também um meio de nos comunicarmos para esclarecer qualquer dúvida que possa surgir. Basta nos enviar um e-mail para contatos@hostmarq.com.br.<br><br>

Este documento refere-se a Lei geral de proteção de dados e tem a finalidade de deixar mais claro possível a nossa política de coleta e compartilhamento de dados, informando sobre os dados coletados e como os utilizamos. Portanto, aplica-se a todos os usuários e visitantes de nosso sistema e integra os Termos e Condições Gerais de Uso do site.<br><br>

Ao utilizar nosso site/plataforma, que é restrito apenas aos membros, você declara o seu EXPRESSO CONSENTIMENTO para podermos armazenar informações sobre você quando julgamos adequado a prestação de nossos serviços, tais como:<br><br>

<b>Informações que você fornece.</b> Podemos coletar dados fornecidos por você no cadastro, tais como nome e sobrenome, endereço para correspondência, endereço de email, outras informações de contato online bem como número de telefone e demais informações requeridas no cadastro ou vinculados às mídias utilizadas em login de acesso.<br><br>

<b>Conteúdos de seu interesse.</b> Podemos coletar o conteúdo das áreas de seu interesse quando você usa nossos Serviços, como quando realiza uma pesquisa, acessa ou cria conteúdos, cadastra uma conta, cria ou compartilha comentários ou conteúdos, etc.<br><br>

<b>Informações de pagamentos.</b> Ao submeter seus dados para pagamento, podemos coletar informações sobre a compra ou transação. Isso abrange suas informações de pagamento, como os dados do seu cartão de crédito ou débito, informações de conta e autenticação, além dos dados de faturamento, tais como endereço e nome completos, CPF e CNPJ.<br><br>

<b>Endereço Eletrônico (e-mail).</b> Ao fazer login em nossos sites/plataformas ou solicitar algum serviço ou produto nosso, coletaremos o seu email para fins cadastrais, pelo qual ocorrerão as comunicações de atualizações de nossos produtos e serviços, nossa plataforma/site, promoções e gerenciamento da sua conta.<br><br>

<b>SEÇÃO 2 – FINALIDADE: UTILIZAÇÃO DESTAS INFORMAÇÕES</b><br><br>

Ao utilizar nossos sites e plataformas você AUTORIZA EXPRESSAMENTE o uso destas informações para as seguintes FINALIDADES:<br><br>

Fornecer, melhorar e desenvolver os Serviços disponíveis. Usamos cookies e tecnologias semelhantes para fornecer e apoiar nossos serviços, através da personalização do conteúdo compartilhado, para garantir que o conteúdo do site/plataforma seja relevante.<br><br>

Construção de conteúdo. Todo conteúdo compartilhado, ou divulgado, seja através de comentários, dicas ou sugestões de melhorias compõem o banco de dados da plataforma viabilizando a melhoria e conhecimento do produto pelos demais usuários.<br><br>

Estratégia de marketing e comunicação;<br><br>

Previsão do perfil do usuário: tratamento automatizados de dados pessoais para avaliar o uso na plataforma;<br><br>

Dados de cadastro. Permitindo o acesso do usuário a determinados conteúdos da plataforma/site, exclusivo para usuários cadastrados;<br><br>

O Clube de Tiro repudia o uso de dados pessoais para fins discriminatórios/abusivos e para obtenção de vantagem econômica e desta forma coloca seu posicionamento a seus terceiros.<br><br>

O tratamento de dados pessoais para finalidades não previstas nesta Política de Privacidade somente ocorrerá mediante comunicação prévia ao usuário, de modo que os direitos e obrigações aqui previstos permanecem aplicáveis.<br><br>

<B>SEÇÃO 3 – FORMAS DE RECOLHIMENTO DOS DADOS PESSOAIS DO USUÁRIO E  VISITANTE</B>

Os dados pessoais do usuário e visitante são recolhidos pela plataforma da seguinte forma:<br><br>

Um link externo é enviado ao interessado que voluntariamente preenche, ele mesmo, todos os campos e submete o formulário ao sistema. Esses dados são os dados de identificação básicos, como e-mail, nome completo, telefone, etc. <br><br>

<b>SEÇÃO 4 – COMPARTILHAMENTO DE DADOS</b>

O Clube de Tiro não irá compartilhar dados pessoais com terceiros, apenas e exclusivamente mediante autorização específica do titular ou em conformidade com ordem judicial.<br><br>

Os dados cadastrais poderão ser compartilhados com prestadores de serviço terceiros com a intenção de permitir o desenvolvimento de suas respectivas atividades e garantir a melhoria dos serviços e operações. Sempre mediante contrato com cláusulas que garantam a proteção e a confidencialidade no processamento dos dados.<br><br>

Compartilhamos os seus dados com empresas parceiras que atuam na mesma linha de fornecimento dos serviços contratados para fins de viabilizar pagamentos e o processamento de dados, bem como para tornar o produto mais relevante e eficiente à sua finalidade.<br><br>

<b>SEÇÃO 5 – ONDE ARMAZENAMOS E PROCESSAMOS SEUS DADOS</b><br><br>

Os dados coletados podem ser armazenados e processados no Brasil e em qualquer outro país escolhido para operar eficientemente, melhorar o desempenho e proteger os dados no caso de uma falha ou outro problema.<br><br>

<b>SEÇÃO 6 – RESPONSABILIDADES DO CONTROLADOR E MEDIDAS DE SEGURANÇA</b>

Todos os dados que você nos fornece são tratados unicamente para atingir as finalidades acima listadas. Estes dados são armazenados em servidores seguros nossos ou de fornecedores contratados, acessados e utilizados de acordo com nossas políticas e padrões de segurança. Todo o tráfego entre nossos servidores ou entre o seu computador e nossos servidores é encriptado através de protocolo seguro SSL ou semelhante.<br><br>

Tomamos medidas de boas práticas e certificações existentes no mercado para garantir que os dados que coletamos sejam processados de acordo com segurança onde quer que os dados estejam localizados.<br><br>

O acesso ao seu perfil é de responsabilidade exclusivamente sua, por meio de uma senha que lhe permite acessar certas partes da plataforma/serviços, sendo de sua responsabilidade manter esta senha confidencial e por cumprir quaisquer outros procedimentos de segurança. Nunca solicitamos sua senha, e pedimos que você não a compartilhe com ninguém.<br><br>

A segurança e confiabilidade dos dispositivos os quais você utiliza para acessar os nossos serviços, tais como computadores, celulares, tablets ou outros dispositivos, é de total responsabilidade sua. Você deve por meios próprios buscar proteger os sistemas operacionais desses dispositivos contra qualquer ameaça externa.<br><br>

<b>SEÇÃO 7 – QUANTO TEMPO VAMOS ARMAZENAR SUAS INFORMAÇÕES</b><br><br>

Nós manteremos as informações que coletamos de você até que ocorra a solicitação de exclusão definitiva por sua parte. Neste caso, nós cessaremos imediatamente a utilização dos seus dados para fins comerciais, porém armazenaremos os seus dados por até 2 (dois) anos após a solicitação, ou enquanto tenhamos obrigações legais ou judiciais a comprir com tais dados.<br><br>

Dados armazenados com base no seu consentimento serão mantidos em nosso banco de dados e processados respeitando o propósito para o qual foram originalmente coletados pelo período estabelecido no termo de consentimento a menos que o titular revogue o consentimento se opondo ao tratamento dos dados.<br><br>

Caso você venha a recadastrar-se no site/plataforma após a solicitação de exclusão de dados, nós poderemos voltar a utilizar os seus dados armazenados. <br><br>

Você pode excluir sua conta a qualquer momento. Mas, lembre-se de que os comentários, informações, cópias e reprodução do conteúdo disponibilizado por você devem ser apagados por você no local publicado.<br><br>

<b>SEÇÃO 8 – DADOS DO CONTROLADOR DOS DADOS</b><br><br>

A gestão do seus dados é feito diretamente pelo Clube de Tiro, e você pode entrar em contato com a pessoa responsável pelos seus dados através do email: contatos@hostmarq.com.br.<br><br>

Você também tem o direito de contatar diretamente a Autoridade de Proteção de Dados brasileira.<br><br>

<b>SEÇÃO 9 – DIREITOS DO TITULAR DOS DADOS</b><br><br>

O titular dos dados pessoais tem direito a obter do controlador, em relação aos dados do titular por ele tratados, a qualquer momento e mediante requisição:<br><br>

I – Confirmação da existência de tratamento;<br><br>

II – Acesso aos dados;<br><br>

III – Correção de dados incompletos, inexatos ou desatualizados;<br><br>

IV – Anonimização, bloqueio ou eliminação de dados desnecessários, excessivos ou tratados em desconformidade com o disposto nesta Lei;<br><br>

V – Portabilidade dos dados a outro fornecedor de serviço ou produto, mediante requisição expressa, de acordo com a regulamentação da autoridade nacional, observados os segredos comercial e industrial;<br><br>

VI – Eliminação dos dados pessoais tratados com o consentimento do titular, exceto nas hipóteses previstas no art. 16 da LGPD;<br><br>

VII – Informação das entidades públicas e privadas com as quais o controlador realizou uso compartilhado de dados;<br><br>

VIII – Informação sobre a possibilidade de não fornecer consentimento e sobre as consequências da negativa;<br><br>

IX – Revogação do consentimento nos termos do &5 do art. 8 da LGPD.<br><br>

<b>SEÇÃO 10 – COMO ACESSAR E CONTROLAR SUAS INFORMAÇÕES PESSOAIS</b><br><br>

Você pode cancelar ou descadastrar-se de forma total ou parcial da plataforma com o pedido expresso de exclusão total de seus dados, por meio do email: contatos@hotmarq.com.br.<br><br>

Você pode igualmente solicitar informações, alterações, esclarecimentos ou exclusão de seus dados por meio de contatos@hostmarq.com.br. Vamos exercer imediatamente as solicitações, nos termos da lei de proteção de dados aplicável.<br><br>

<b>SEÇÃO 11 – CONSENTIMENTO</b><br><br>

Ao utilizar os produtos/serviços e fornecer as informações pessoais na plataforma/site, o usuário está consentindo com a presente política.<br><br>

O usuário, ao cadastrar-se, manifesta conhecer e pode exercitar seus direitos de cancelar seu cadastro, acessar e atualizar seus dados pessoais e garante a veracidade das informações por ele disponibilizadas.<br><br>

<b>SEÇÃO 12 – ALTERAÇÕES PARA ESSA POLÍTICA DE PRIVACIDADE</b><br><br>

Reservamos o direito de modificar este documento a qualquer momento, então, é recomendável que o usuário e visitante revise-o com frequência.<br><br>

As alterações e esclarecimentos vão surtir efeito imediatamente após sua publicação na plataforma/site. Quando realizadas alterações os usuários serão notificados. Ao utilizar o serviço ou fornecer informações pessoais após eventuais modificações, o usuário e visitante demonstra sua concordância com as novas normas.<br><br>

